package Lista01;
/*
 A trilogia de um filme de terror inclui os filmes "Zumbis Duram para Sempre", que dura 80 minutos, "Um lobisomem Americano 
 em Bermudas", que possui 95 minutos e "O Lanchinho da Noite do Vampiro", com 115 minutos do início ao fim. Qual é a relação 
 total dos três filmes?
 */
public class aula01 {
	public static void main(String[] args) {
		int zumbi = 80;
		int lobizomen = 95;
		int vampiro = 115;
		
		int somaTotal = (zumbi + lobizomen + vampiro);
		System.out.println("Total em minutos dos 3 filmes: " + somaTotal);
	}
}
